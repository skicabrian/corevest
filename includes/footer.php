    <script>
    (function(){
        // Minimal scroll reveal using IntersectionObserver. Adds subtle fade+slide to sections and cards.
        document.addEventListener('DOMContentLoaded', function(){
            try {
                if (window.matchMedia && window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
                    document.querySelectorAll('section, .max-w-sm').forEach(function(el){ el.classList.add('active'); });
                    return;
                }

                var sections = Array.from(document.querySelectorAll('section'));
                var cards = Array.from(document.querySelectorAll('.max-w-sm'));
                var extras = Array.from(document.querySelectorAll('.mr-auto.place-self-center, .space-y-8 > div'));
                var targets = sections.concat(cards, extras);

                targets.forEach(function(el, i){
                    if (!el.classList.contains('reveal')) {
                        el.classList.add('reveal');
                        var d = Math.min(i * 60, 600);
                        el.setAttribute('data-reveal-delay', d);
                    }
                });

                var io = new IntersectionObserver(function(entries, obs){
                    entries.forEach(function(entry){
                        if (entry.isIntersecting) {
                            var el = entry.target;
                            var delay = el.getAttribute('data-reveal-delay') || 0;
                            el.style.transitionDelay = delay + 'ms';
                            el.classList.add('active');
                            obs.unobserve(el);
                        }
                    });
                }, { threshold: 0.12 });

                document.querySelectorAll('.reveal').forEach(function(el){ io.observe(el); });
            } catch (e) {
                console.error('reveal init error', e);
            }
        });
    })();
    </script>

